  var FAV_KEY = 'yalla-kids:favorites';
  function favRead() {
    try {
      var raw = localStorage.getItem(FAV_KEY);
      if (!raw) return [];
      var arr = JSON.parse(raw);
      return Array.isArray(arr) ? arr : [];
    } catch (e) { return []; }
  }
  function favWrite(arr) {
    try { localStorage.setItem(FAV_KEY, JSON.stringify(arr)); } catch (e) {}
    try { window.dispatchEvent(new CustomEvent('yalla:favorites', { detail: { list: arr.slice() } })); } catch (e) {}
  }
